# if Statement
name = "Sree"
print("Hello, Sree!" if name == "Sree" else "Hello, stranger!")

# if-else Statement
name = "Sree"
print("Hello, Sree!" if name == "Sree" else "Hello, stranger!")

# if-elif-else Statement
name = "Sree"
if name == "Sree":
    print("Hello, Sree!")
elif name == "Alice":
    print("Hello, Alice!")
else:
    print("Hello, stranger!")

# while Statement

count = 0
while count < 5:
    print(f"Count is: {count}")
    count += 1

# for Statement

for count in range(5):
    print(f"Count is: {count}")
    
